const TEMPLATE_KEY: Record<string, string> = {
  resume_aero_minimal: 'template.aero_minimal',
  resume_golden_dual: 'template.golden_dual',
  cv_bento: 'template.bento',
  cv_cli: 'template.cli',
}

export function getTemplateLabel(
  templateId: string,
  locale: Locale
): string {
  const key = TEMPLATE_KEY[templateId]
  return key ? translations[locale][key] ?? templateId : templateId
}
